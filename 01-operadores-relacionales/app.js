var numero = 3;
var numero2 = 2;
// estrictamente igual
var respuesta = numero === numero2;
console.log(respuesta);
var respuesta2 = numero == numero2;
console.log(respuesta2);
var respuesta3 = numero != numero2;
console.log(respuesta3);
// estrictamente diferente
var respuesta4 = numero !== numero2;
console.log(respuesta4);
var respuesta5 = numero < numero2;
console.log(respuesta5);
var respuesta6 = numero <= numero2;
console.log(respuesta6);
var respuesta7 = numero > numero2;
console.log(respuesta7);
var respuesta8 = numero >= numero2;
console.log(respuesta8);
